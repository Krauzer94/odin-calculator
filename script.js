// Functions for each math operation
// Add, subctract, multiply and divide
// Test tem directly into the console

// Add operation
function add(a, b) {
    return a + b;
}

// Subtract operation
function sub(a, b) {
    return a - b;
}

// Multiply operation
function mul(a, b) {
    return a * b;
}

// Divide operation
function dvd(a, b) {
    return a / b;
}

// Test numbers
let a = 1;
let b = 2;

// Log validation
console.log(`Numbers: 1 and 2`)
console.log(`Adding: ${add(a, b)}`);
console.log(`Subtracting: ${sub(a, b)}`);
console.log(`Multiplying: ${mul(a, b)}`);
